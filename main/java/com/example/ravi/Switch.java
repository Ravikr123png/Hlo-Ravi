package com.example.ravi;

public class Switch {
    public static void main(String[] args){
        int month=1;
        switch (month){
            case 1:
                System.out.println(month +"jounery");
                break;
            case 2:
                System.out.println(month +"feb");
                break;
            case 3:
                System.out.println(month +"mah");
                break;
            case 4:
                System.out.println(month +"apl");
                break;
            case 5:
                System.out.println(month +"may");
                break;
        }
    }
}
